package com.reserv.model;

public class DataModel {
    double coAmount;
    double tempAmount;
    double humidAmount;
    double airAmount;

    public double getCoAmount() {
        return coAmount;
    }

    public void setCoAmount(double coAmount) {
        this.coAmount = coAmount;
    }

    public double getTempAmount() {
        return tempAmount;
    }

    public void setTempAmount(double tempAmount) {
        this.tempAmount = tempAmount;
    }

    public double getHumidAmount() {
        return humidAmount;
    }

    public void setHumidAmount(double humidAmount) {
        this.humidAmount = humidAmount;
    }

    public double getAirAmount() {
        return airAmount;
    }

    public void setAirAmount(double airAmount) {
        this.airAmount = airAmount;
    }
}
